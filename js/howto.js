  /**
   * 遊び方の中身を、渡された場所に作る。
   * @param {HTMLElement} root - 中身を入れる箱
   */
  build: function (root) {
    if (!root) return;
    root.innerHTML = '';

    this.SECTIONS.forEach(function (sec) {
      const box = document.createElement('details');
      box.className = 'howto__sec';
      if (sec.open) box.open = true;

      const head = document.createElement('summary');
      head.className = 'howto__head';
      head.textContent = sec.title;
      box.appendChild(head);

      sec.blocks.forEach(function (b) {
        const item = document.createElement('div');
        item.className = 'howto__item';

        const t = document.createElement('div');
        t.className = 'howto__term';
        t.textContent = b[0];
        item.appendChild(t);

        const d = document.createElement('div');
        d.className = 'howto__desc';
        d.textContent = b[1];      // 改行はCSSの white-space で活かします
        item.appendChild(d);

        box.appendChild(item);
      });

      root.appendChild(box);
    });
  },
